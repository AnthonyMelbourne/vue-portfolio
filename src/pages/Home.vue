            PlayerUnknown’s Battlegrounds didn’t invent the last-person-standing
            formula that’s made it one of the most massively successful PC games
            in history, but – over the course of a nine-month-long early access
            period – it certainly turned it into something special. 100 players
            drop onto a sprawling map and fight to the death using whatever
            weapons they can find within a shrinking force-field. This simple,
            bare-bones take on the Battle Royale-style deathmatch foregoes the
            fluff and high time investment of most hardcore survival sims in
            favor of fast, accessible action. The result is a tight, focused,
            no-frills experience that’s as exciting as it is unpredictable, and
            where no two games are ever alike.
            <br /><br />
            PUBG wastes no time putting you right in the middle of the action as
            you and up to 99 other players airdrop onto one of its two 8x8 km
            maps from the back of a large aircraft. Part of the genius of each
            match’s arc is how heavily it weighs every move you make, and it
            begins with the critical decision of where to land. Jumping early
            along the plane’s randomized flight path lets you hit the ground
            first, giving you a jump on the competition, but risks placing you
            farther from the parts of the arena where the fighting will likely
            funnel as the boundaries contract. Jumping late might distance you
            from other players, giving you some early breathing room and a
            chance to forage for loot in peace before entering the fray.
            Depending on the angle of the flight path, jumping right into the
            center — especially in large cities and other loot-heavy spots —
            could place you right in the middle of the action with some of the
            most trigger-happy opponents.
            <br /><br /><img
              src="./assets/images/games/promo/pubg-promo1.jpg"
              alt="PlayerUnknown's Battlegrounds Promo Photo"
              class="feature-one-promo"
            />
            The tension mounts during the descent as you get a good look at the
            swarm of players in your vicinity, but things really pick up the
            moment you hit the ground, as everybody (no matter where you land)
            scrambles for weapons and gear, and the killing kicks off right from
            the get-go. Item distribution is partially randomized, and learning
            the spots on the maps that are likely to spawn military-grade
            weapons and attachments is a good, early strategy to work out on
            your own, with a team of up to four, or using the many fan-made
            resources that already exist thanks to PUBG’s hearty run in Early
            Access. You can’t count on finding a good gun early to keep you
            safe, but unlike similar games like DayZ, where you could
            potentially go long, tedious stretches without finding a weapon,
            there’s bound to be something worth picking up no matter where you
            drop. Backpacks for holding more items, a handful of protective gear
            like helmets and police vests, medical supplies, melee weapons, and
            a good mix of firearms are in good supply.
            <br /><br />
            But no matter what gear you find, PUBG excels at creating a reliable
            and streamlined space for your adventure to play out while also
            allowing for a thrilling level of unpredictability. It isn’t out of
            the ordinary for someone with only a pistol to take down someone
            with a fully loaded SCAR-L assault rifle, since good gear is only
            half the battle — the other half is wit.<br />
            As players outsmart each other in deadly games of cat and mouse,
            those still standing are forced into ever-closer proximity within
            the confines of a deadly, shrinking force-field until just one
            player or team is left alive. Victory (accompanied by the coveted
            “Winner, winner, chicken dinner!” screen) is an exhilarating reward,
            matched only by the suspense that emerges from the battle to achieve
            it. But even if you die early, jumping into a new game is always
            quick and easy.
