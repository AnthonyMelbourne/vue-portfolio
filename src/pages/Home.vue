            saddle, or carefully choosing your customized arsenal before a
            firefight brings you closer to the lifestyle of the era, and Arthur
            himself. These actions help you build personal attachments to
            everything, and the small doses of reality never slow things down
            too much or spoil the experience.
            <br /><br />
            Fast travel also has its limits. You can take trains and
            stagecoaches to towns you’ve discovered, and you can purchase the
            ability to skip to a location directly from your bedside, but you
            can no longer zip to somewhere from a campfire on the open road.
            Campfires instead now allow you to craft special types of ammunition
            and cook meat you find while hunting.
            <br /><br />
            The extra riding required to get everywhere leads to some of the
            game’s most rewarding and spontaneous moments. Animal parts you
            collect from hunting can be used to upgrade your inventory space,
            create stat-boosting trinkets, feed the gang, or net you extra cash.
            Strangers, and other random events, pop up constantly. These might
            open up new types of items to go searching for, give you a chance to
            adjust your honor meter, or alert you to other means of making
            money. You’re free to help, rob, loot, kill, or ignore these people
            as you see fit.
            <br /><br /><img
              src="./assets/images/games/promo/red-dead-redemption-2-promo2.jpg"
              alt="Red Dead Redemption 2 Promo Photo"
              class="feature-one-promo"
            />
            Different gangs also occupy each area. They’ll hassle you or the
            locals and set up traps for you to spring on the trail. Dead Eye
            returns to help you pick off targets in slow motion, but now there’s
            also Eagle Eye for gathering plants, searching for clues, and
            tracking beasts. All the info you acquire is stored in a compendium,
            and certain memories are preserved in Arthur’s journal. It’s
            satisfying to fill in each part of the map and discover new towns,
            but things are still happening in places you’ve already visited and
            surprises keep popping up even after the end credits roll. If you
            need a break from the outlaw life, you can play an assortment of
            card and dice games, see a live show or movie, or bring in a bounty
            and partake in that set of unique stories and colorful characters.
            <br /><br />
            This world is alive with people to meet and animals to find, and
            your methods of interacting with them are impressive, but the game’s
            visuals transport you to another time and place more than anything
            else. The customization options with your weapons, clothing, and
            horses are of the highest quality and they’re easy to obsess over.
            Hair takes time to adjust as you have to grow it before you can cut
            it. The lighting has clearly received special attention,
            particularly when rays from the sun cut through foggy forests, rain
            dumps down, and lightning crackles in the distance. Likewise, towns
            become illuminated with a soft glow against the heavy darkness of
            night. Fire appears to completely consume certain objects when lit,
            wood splinters as you use boxes or posts for cover in gunfights, and
            new buildings are constructed as the story goes on.
            <br /><br />
            Navigating the map is one wow moment after another, and just when
            you start to get used to how beautiful the game is, it hits you
            again with another stunning view. Even little animations like when
            Arthur puts his rifle over his shoulder or tucks it into the saddle
            give all the elements a satisfying weight and relevance. A
            first-person view lets you play from a different perspective, and
            while we didn’t switch to it that often, it is amusing to keep a
            close eye on people while each scene plays out.
            <br /><br />
            Red Dead Redemption 2 tests the boundaries of interactive immersion.
            Few games look this good, play this well, and have this much to
            offer. It begs you to slow down, turn every page of its dramatic
            tale, and savor every last breathtaking detail. It challenges you to
            be prepared for anything, and make the right decisions when your
            choices matter most. Arthur Morgan is the lead, but the world takes
            center stage to pull you in. With a list of challenges left to
            finish and an online mode launching soon, we plan to spend a lot
            more time in Rockstar’s unparalleled recreation of the old west.<br />
            <span>Score: 10 / 10</span>
