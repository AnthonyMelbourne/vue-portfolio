            For some reason duel vocal deathcore, or extreme metal in general
            has been very few and far between – genre luminaries
            <span>Despised Icon</span> as well as newer bands such as
            <span>AngelMaker</span>, have proved that it could be done and done
            well, for whatever reason the trend just never took, but
            <span>BABIRUSA</span> clearly never got that memo – going from
            shrill black metal highs to deathcore signature mid-range vocal
            style to brutal death’s guttural lows, all while exhibiting a song
            writing and production quality that is at an international level,
            one would not expect any less from the very capable hands of
            <span>Sean Delander (Thy Art Is Murder)</span> and
            <span>Dan Field</span> at the helm, handling production and
            engineering duties respectively. <br /><br />
            You can’t talk about this album without mentioning its concept – Man
            vs Machine – Not necessarily a original concept by any means but one
            that is still incredibly relevant in this day and age, a concept
            that has transcended generations and all form of media, through
            books (Dune, The Humanoids, Do Androids Dream Of Electric Sheep?
            etc.), film (The Matrix and Terminator franchises, Blade Runner
            etc.), television (Black Mirror etc.) and music (Fear Factory etc.)
