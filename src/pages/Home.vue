            Deathcore upstarts <span>BABIRUSA</span> have made quite the name
            for themselves in their short existence, positioning themselves as
            forerunners for the deathcore crown in Australian heavy music. After
            releasing their first single in mid-2019 and subsequent singles in
            early-2020, they gained themselves quite a lot of hype, especially
            considering that the band is made up with current and ex-members of
            local favourites <span>Tomb of Doom</span>,
            <span>I, Valiance</span>, <span>A Night in Texas</span>,
            <span>The Senate</span> and <span>Crowned in Flesh</span>, so
            understandably there would be a certain amount of expectation that
            whatever comes out of these guys is going to be undeniably brutal
            and heavy. <br /><br />
            Their sound is very easily categorized as technical deathcore along
            the lines of
            <span>Aversions Crown</span> and <span>Shadow of Intent</span>, with
            strong elements of technical and progressive death metal intertwined
            with swathes of melodic death and traditional deathcore with a hint
            of slam – transitioning from sweep picked arpeggios, palm muted
            chugs and slams to blast beats and lightning fast drum fills with
            ease – with an emphasis on crafting intricate, thoughtful and catchy
            riffs and let’s not forget the use of two vocalists. <br /><br />
            <img
