            The missions team you up with individual gang members right away and
            they keep giving you chances to understand each person’s
            perspective, whether it’s a black man confronting racism, or a
            Native American watching the government betray his people. You’re
            given a lot of choice, and certain objectives can be skipped if
            you’re not in the mood. Conversations evolve depending on the order
            you complete quests, and prior events are often referenced by the
            people you work with. Outside of these jobs, you can catch up with
            your associates back at camp.
            <br /><br />
            Your movable hideout functions as a conduit for each chapter’s
            events. You can chat with friends to pass the time and upgrade the
            camp visually with perfectly harvested animal skins. You can add or
            improve the available supply storage, and other members will donate
            money to the cause when they can. With hours of animation and
            dialogue to set each scene, the camp is impressively realized, and
            it’s delightful to pause your progress and slowly sip a cup of
            coffee on a cold morning or sit and listen to a guitar by the
            campfire after a rough day.
