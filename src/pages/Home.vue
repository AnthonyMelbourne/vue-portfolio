            million little moments just waiting to surprise you. You can provoke
            your surroundings by targeting people, animals, or objects with the
            left trigger. Your choices shift depending on the context, so you
            use the same lock-on method to brush your horse, rob a store, or
            talk to strangers. Whether you chat with every person or not, it
            feels good to know the option is there. Gang members will only have
            one or two-line replies at any given time, but even that adds a
            tremendous amount of character to the proceedings.
