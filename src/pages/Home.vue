            The Black Dahlia Murder vibes, which I really enjoy, a welcome
            injection of melody in the almost anxiety inducing riffage returns
            before the massive breakdown, building up and gaining momentum,
            culminating in a guitar solo and sinister tremolo riff and blast
            beat section that eventually fades into oblivion. <br /><br />
            ‘CATATONIA’, the third single begins with both vocalists proclaiming
            “We are the only ones who see the world for what it is!” before the
            rest of the band joins in, luring the listener into a false sense of
            security before changing the tempo from a mid-paced groove section
            to lightning fast tech death, slowing down just enough for a slow,
            menacing build up allowing you catch your breath and to believe for
            a second that a breakdown was coming but instead, speeding up even
            faster than before, until completely slowing down for an epic guitar
            solo and an almost discordant black metal tremolo riff section,
            closing out the track with a sample; the continuation of the story
            leading into ‘REFLECTION’, a short instrumental giving us further
            insight into the story of ‘343’.
