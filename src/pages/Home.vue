            <span>IN THE ZONE</span>
            The deadly, moving force-field that contains the arena and
            occasional airstrikes marked with red on the minimap add a
            consistent sense of purpose that drives you toward conflict, and
            with it, an exciting set of options. You might want to prioritize
            reaching the center of the circle to make the next incremental
            reduction in size of the survivable area easier to deal with; move
            slowly along the borders to pick off stragglers as they rush into
            the open to avoid the oncoming doom; or even hide in a building
            until you absolutely have to move, letting everyone else kill each
            other so you don’t have to. Hopping into a vehicle, like the speedy
            but vulnerable motorbike and buggy, the sturdy closed-top UAZ, or
            the slow-but-durable van — among others — is a quick, fun, and
            conspicuous way to cover a lot of ground, and can result in some
            hilarious and explosive encounters. Either way, you’re encouraged to
            stay on the move, but still have a set of options that lets you pick
            your pace. An aggressive approach is just as viable as a cautious
            defense, and the practicality of both playstyles is refreshingly
            liberating, bringing new sets of challenges that are just as fun to
            plan around and tackle as the next.
            <br /><br />
            The incentives to stay on the move no matter your chosen pace forces
            ever more dangerous encounters with increasingly skilled (or lucky)
            survivors, condensing the action and ending a match after around 20
            minutes, before it can get bogged down by campers or other
            distractions. Action isn’t hard to find if you’re looking for it,
            and impossible to avoid for long when you’re not, but PUBG manages
            to put enough space between encounters to keep those quiet, tense
            moments of cautious exploration intact.
            <br /><br />
            The two large maps aren't the best-looking environments in gaming,
            but they do provide much-needed variety on the battlefield. Both the
            forested island of Erangel and the sunny desert of Miramar boast an
            assortment of small towns, large, dense cityscapes, and rural
            industrial zones ripe with loot. There are some parallels between
            maps so as not to restrict your options when you’re dropped randomly
            into either one (the server picks for you). For instance, the small
            military base on an island off the coast of Erangel sees its match
            in Miramar’s remote military camp, for the high-risk players looking
            to compete for high-level equipment right off the bat.
            <br /><br />
            But both maps have their own unique merits, too. The abundance of
            trees on Erangel provides plenty of cover, even for groups of four,
            while the sparser, rockier Miramar makes moving across open areas on
            foot and in vehicles much more dangerous. While some areas in
            Miramar feel more isolated than even the most secluded spots on
            Erangel, the diversity of elevations, more interesting city layouts,
            and some great map-exclusive weapons — like a sawed-off shotgun that
            fits conveniently in the sidearm slot — more than make up for it.
            Adapting to new challenges and making last-minute decisions based on
            each of these factors is part of the fun. But no matter what you’re
            facing, an abundance of loot in even the smallest buildings in both
            maps ensures hunting for a weapon is never a frustrating task, which
            not only keeps the playing field balanced and fun for everyone, but
            ripe for all sorts of thrilling confrontations.
            <br /><br />
            You might find yourself involved in a frantic shootout in the woods,
            or on either end of a deadly hunt across a dense city or town. You
            might even find yourself holed up in an attic, listening for the
            slightest creak or scape to indicate a nearby presence, shotgun in
            hand. The use of sound to build tension and alert you to nearby
            enemies is one of the ways PUBG forces you to stay attentive and
            tactical rather than treating every round like a fast-paced shooter
            à la Call of Duty or Battlefield. Play long enough, and you can
            learn the subtleties that tip off when another player has been in a
            place before you — an open door or an unusually dense or sparse pile
            of mismatched loot are telltale signs you should watch your back.
            All these layers merge to build a shooter that’s invested in
            creating smart, nuanced survival and understands how even the
            smallest moments of your story build up to create the larger
            adventure.
            <br /><br />
            <span>Last One(s) Standing</span>
            The entire dynamic of both exploration and combat changes
            drastically by grouping up in the pre-game lobby. Playing solo
            brings certain elements to the formula that going in with a partner
            doesn’t, and the same goes for trios and four-player squads. There
            might be more freedom and greater opportunity for stealth in going
            alone, but a well-coordinated team has more potential for pulling
            off exciting ambushes and vehicular assaults – and they’re much more
            survivable because you can revive a teammate who’s been knocked down
            but not finished off. Playing in first-person-only servers also
            brings a new, challenging dimension to matches by eliminating the
            corner-peaking advantages granted by the default third-person
            camera.
            <br /><br />
            The gunplay itself is also great. Firearms feel good to shoot and
            there’s a good range of options for all playstyles. And while PUBG
            does retain that military-sim stiffness that’s become synonymous
            with the genre, it’s just smoothed out enough to keep combat and
            movement satisfying and accessible to newcomers. The new vaulting
            system (added as PUBG left Early Access) is a particularly handy
            addition, allowing you to climb over fences and through windows for
            easier getaways and sneakier shortcuts.
            <br /><br />
            Other than that, a couple handy hotkeys to cycle weapons, switch
            between third- or first-person perspective, toggle auto or burst
            fire for your gun, and adjust zeroing distance are most of what you
            need to know beyond the obvious. The simple and intuitive UI is also
            particularly refined, which makes looting bodies and swapping items
            with teammates as easy as a quick click and drag.
            <br />
            A number of quality-of-life features round out even PUBG’s roughest
            edges: being able to access your inventory and map while on the
            move, adjust the camera mid-stride without changing direction, and a
            generous leniency with fall damage stand out as some of the most
            valuable. Since the 1.0 launch, I haven’t experienced significant
            performance or rubberbanding issues, but it’s important to note that
            version 1.0 still hasn’t managed to eliminate the hacking problem
            that’s most prevalent in high-skill matchmaking games, or a handful
            of persistent bugs.
            <br /><br />
            <span>Verdict</span>
            PlayerUnknown’s Battlegrounds has taken the military-sim gameplay
            popularized by games like ARMA and DayZ, boiled it down to its most
            exciting parts, and streamlined it into quick and accessible rounds
            of pure, hassle-free, survival-based action. Even though each game
            starts the same way, its remarkable ability to feel like a new,
            tense adventure each and every round has kept me coming back for
            hours on end.
            <br /><br />
