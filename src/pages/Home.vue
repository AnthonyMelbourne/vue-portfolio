            Segueing on from track 5 – ‘PENDULUM SWING’, ominous clean guitar
            strums behind a sample telling the main character, “It’s time to
            become a part of all things…”, launching into a very catchy groove
            orientated and bouncy djent chug riff that is sure to get the crowd
            moving – then putting the foot on the gas with a chaotic riff
            section that winds and swirls, stopping and starting on a whim,
            maintaining the groove until crowning with an earth shattering
            breakdown.
            <br /><br />
            ‘RESISTOR’ wastes no time, getting right to business, with an
            astonishingly fast and elaborate riff that would feel right at home
            on a <span>Deeds of Flesh</span> or
            <span>Decrepit Birth</span> record, which is just fantastic, Tate
            and Dean shine especially bright here, the riffs are complex and
            have a neo-classical quality to them, especially with the quiet
            choir effect behind them – an accompanying chug part with the
            appropriate amount of guitar bends for full effect before breaking
            into a quick jazz interlude that merely stalls long enough to
            demolish you with an absolutely crushing breakdown, fading out and
            into ‘ABOMINATION’, the second single maintains a mid-paced tempo
            and bouncy feel for that keeps up the intensity and groove, while
            the vocalists execute rapid fire vocal attacks back and forth,
            Rangi’s drumming is particularly impressive here. <br /><br />
