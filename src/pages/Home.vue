            You control outlaw Arthur Morgan, an elite member of the Dutch van
            der Linde gang. You start in the snow-packed mountains after barely
            surviving a botched robbery. Dutch and family take shelter from the
            harsh weather while plotting to regain their fortune and seek
            revenge on their enemies. As you make more money and cause more
