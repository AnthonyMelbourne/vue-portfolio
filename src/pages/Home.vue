            Unlike the bounty-focused revenge crusade in Red Dead Redemption,
            Arthur’s story is more about secrecy and survival. Most missions are
            done to fill your pockets with cash and grow the gang. While you
            won’t learn much about Arthur’s past beyond a few news clippings,
