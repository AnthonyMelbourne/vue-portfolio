            like a truck, this one is sure to be a crowd favourite, while
            ‘ZEROHOUR’, the final outro track, laden with samples and epic
            guitar harmonies concluding our protagonist, ‘343’s fateful journey
            of self-discovery and alienation. <br /><br />
            Overall, this is a phenomenal debut album – ultimately a bold choice
            to do a concept record for your first, but given the experience in
            song writing that the various members possess, it’s not surprising
            that they pulled it off in spades.
