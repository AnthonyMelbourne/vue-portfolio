            Nowadays, Bill and Ted are getting on in years, and their band, Wyld
            Stallyns, has suffered a cataclysmic loss of popularity. The apples
            of the men’s respective eyes are their music-mad twentysomething
            daughters, each of whom has effectively been given the name of their
            dad’s best friend: Thea (Samara Weaving) and Billie (Brigette
            Lundy-Paine). This crossover further seals their friends-for-life
            dedication. Each young woman is of course weirdly similar to her old
            man, a single-joke mannerism that restricts these performers a fair
            bit.
            <br /><br />
            The terrible crisis arrives when Bill and Ted are summoned into the
            distant future by Kelly, and informed by the universe’s Great Leader
            (an amusingly imperious Holland Taylor) that they have to compose a
            great track to bring the world together and prevent the destruction
            of space and time. The boys, sublimely confident that they can do
            it, but unwilling to go through the faff of actually doing it,
            simply decide to time-travel forward to the point when it has been
            done and snatch the composition from their future selves. As Bill
            declares to Ted: “You have had many counterintuitive ideas over the
            years, but this is the counterintuitivest of all.” But the Great
            Leader has an alternative plan to save the world, simply to kill
            Bill and Ted, for which she has trained a neurotic robot assassin
            called Dennis, channelling Douglas Adams’s Marvin the Paranoid
            Android — a nice turn from Anthony Carrigan.
